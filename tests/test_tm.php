<p><br />
Feel free to nab the <a href="/js/TransparentMessage.js">source code</a>.</p>

<p><strong>Conclusion</strong></p>

<p>Transparent messages are a simple and elegant solution to a problem that is normally either over-looked or over-engineered. They're not right for everything, but for messages that need no user interaction, transparent messages are hard to beat: they have an efficiency of 100%.</p>

<p>Plus, they look pretty.</p>
		
