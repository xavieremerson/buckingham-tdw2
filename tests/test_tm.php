		<a href="http://www.humanized.com/weblog/2006/09/11/monolog_boxes_and_transparent_messages/"><h1>Monolog Boxes and Transparent Messages</h1></a>
		
		<p class="s b up"><a href="http://www.humanized.com/weblog/our_products/">Our Products</a>, <a href="http://www.humanized.com/weblog/redesign/">Redesign</a></p>
		
		<p><img style="float:right;" alt="SpellCheckComplete.png" src="http://www.humanized.com/weblog/images/SpellCheckComplete.png" width="309" height="146" /></p>

<p>We've all seen dialog boxes that look like the picture on the right.</p>

<p>Dialog boxes are bad enough: they pop-up at inconvenient times, they derail our train of thought, and normally we don't even read them. But this type of dialog box is worse. It's not even a dialog box. It's <i>monolog</i> box. There's nothing one can do with this messages but click "OK". Or wait and click "OK". They have, as I've explained before, <a href="http://www.humanized.com/weblog/2006/07/22/know_when_to_stop_designing_quantitatively/">0% efficiency</a>. Beyond that, monolog boxes can have frightening consequences: because I often use two monitors, I've spent panicked seconds thinking I'd lost my work due to a crash&#0133; only to discover later that a monolog box had appeared on the other screen and stopped Word from operating normally. Even with a single monitor, you can miss the dialog box, and you'll have a similar scare.</p>
        
		<p>In the article <a href="http://blogs.msdn.com/jensenh/archive/2006/06/14/629189.aspx">The Spelling Check is Complete</a> by Jensen Harris (of the Microsoft Office User Experience team) defends the "Word has finished spell check" monolog box shown above, along with the "Word has finished searching your document"  box. The Office team had tried removing those documents but discovered that "people who were spell checking their document manually had no idea when the process was complete." Thus, the monolog boxes were reinstated.</p>

<p>This is classic inside-the-box thinking. The problem is valid: people have trouble knowing the process is complete. But the solution isn't to use monolog boxes. I can think of two different&mdash;and better&mdash;solutions. You can probably think of more.</p>

<p><strong>Solution 1: Bypass the problem</strong></p>

<p>Implied in the justification for reinstating the monolog boxes is the false idea that spell check is a fundamentally modal operation. In Jensen's words:</p>

<p>"Spell check is one of those great features that have a beginning, a middle, and an end. The beginning is you clicking the spell check button. The middle is the computer conversing with you about potential misspelled words and giving you an opportunity to fix them. And the end is the computer telling you that the process is complete."</p>

<p>But Spell check isn't necessarily a modal interaction at all. Word's own spell-check-as-you-type feature is a great example of a non-modal spell check. The text is underlined with a red squiggle: you only need to "converse" with the computer if you decide to fix it. The Gmail spell check, although ever-so-slightly modal, is another example of a spell check that does not require a "conversation": spell check is done when <i>you</i> are done. Besides obviating monolog boxes, non-modal spell check has another benefit: it isn't mutually exclusive with writing. If you are correcting a misspelling and you see something else you'd like to change, you can do it without exiting spell check. Your <a href="http://humanized.com/about/#rule5">train of thought</a> is never endangered&mdash;let's see the standard Word spell checker and it's monolog box do that!</p>

<p><strong>Solution 2: Think outside the (dialog) box</strong></p>

<p>If you keep Word's somewhat antiquated spell check mechanism, then it's true that the user needs to be informed when Word has finished spell checking. As I've explained, a modal monolog boxes are dangerous and inhumane. Luckily, there are many other methods of presenting information that are not modal. In particular there is one we are using in Enso, our upcoming product: transparent messages.</p>

<p>Transparent messages are the brainchild of <a href="http://www.jefraskin.com">Jef Raskin</a>. It's simply a large and translucent message that's displayed over the contents of your screen. They fade away when the user takes any action (like typing or moving the mouse). In practice, the message is both noticeable yet unobtrusive. And because the message is transparent, you can see what's beneath it. It's just humane. Take a look:</p>

<p><object width="425" height="350"><param name="movie" value="http://www.youtube.com/v/XcHjA5uwHM8"></param><embed src="http://www.youtube.com/v/XcHjA5uwHM8" type="application/x-shockwave-flash" width="425" height="350"></embed></object></p>

<p></p>

<p></p>

<p><br />
<strong>Message Log</strong></p>

<p>Transparent messages, however, introduce a problem: messages disappear easily. What happens if the user wants to see what it said? The solution is message log&mdash;simply a list of past messages. This way if the user doesn't have time to read a message, they can go to the message log to view it only if they think it is important enough. And think about it: a message log would be a great thing to have anyway. How many times have you wanted to reference the contents of a dialog box and couldn't because you had already dismissed it? And how many times have you had to transcribe the contents of a dialog box because the text wasn't selectable? The lives of users and the lives of tech support staff would be greatly simplified by the addition of a system-wide message log.</p>

<p><strong>On the web</strong></p>

<p>The transparent message also has a place on the web. Web designers have a constant struggle to come up with non-obtrusive means of conveying fleeting feedback to users. For example, validating user input and checking login credentials. It is in instances like this that the transparent message really shines. Give it a try below.</p>

<p>The correct email is <b>trans@parent.com</b> and the correct password is <b>pass</b>. Make sure to play with incorrect login information.<br />
